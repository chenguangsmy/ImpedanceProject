% RecipStretch1Term

function Y = RecipStretch1Term(B,X)

b0 = B(1);

b2 = 0.040627458201029 + b0.*(-1.082083046700132);

if b0 < 0.911994658978910
    b1 = -0.006075705680117 + b0.*(-0.070463389869430);
elseif b0 < 1.200592006886346
    b1 = -0.068857005602490 + (b0-0.911994658978910).*(-0.049863646352518);
elseif b0 < 1.496439991669683
    b1 = -0.082271437467774 + (b0-1.200592006886346).*(-0.012977792169351);
else
    b1 = -0.083697935445375 + (b0-1.496439991669683).*(0.214835667052871);
end

Y = (1+b2)./(X) + b0 + (b1*(X-1));

Y(Y>1) = 1;

end